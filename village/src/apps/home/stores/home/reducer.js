/* ========================================================================== */
/* Import                                                                     */
/* ========================================================================== */
import * as actions from './actions';

/* ========================================================================== */
/* State                                                                      */
/* ========================================================================== */
const initialState = {
    message: "welcome",
};

/* ========================================================================== */
/* Reducer                                                                    */
/* ========================================================================== */
const reducer = (state = initialState, action) => {
    switch (action.type) {
        case actions.WELCOME:
            alert(state.message);
            return state;
        default:
            return state;
    }
}

export default reducer;